CREATE DATABASE CRUD_KAP;
USE CRUD_KAP;

CREATE TABLE PROFILE_ (
USERNAME VARCHAR(40) PRIMARY KEY,
PASSWORD_ VARCHAR(40),
EMAIL VARCHAR(40) UNIQUE ,
USER_CODE INT AUTO_INCREMENT UNIQUE,
NAME_ VARCHAR(40),
TELEPHONE VARCHAR(9),
CONSTRAINT CHK_TELEPHONE
        CHECK (TELEPHONE REGEXP '^[0-9]{9}$'),
SURNAME VARCHAR(40)
);

CREATE TABLE USER_ (
USERNAME VARCHAR(40) PRIMARY KEY,
GENDER VARCHAR(40),
CARD_NUMBER VARCHAR(24),
CONSTRAINT CHK_CARD_NUMBER
        CHECK (CARD_NUMBER REGEXP '^[A-Z]{2}[0-9]{22}$'),
FOREIGN KEY (USERNAME) REFERENCES PROFILE_ (USERNAME),
FOREIGN KEY (CARD_NUMBER) REFERENCES CARD (CARD_NUMBER)
);

CREATE TABLE CARD (
CVV INT,
    CHECK (numero BETWEEN 0 AND 999),
EXPIRATION_DATE DATE,
CARD_NUMBER VARCHAR(24) PRIMARY KEY,
CONSTRAINT CHK_CARD_NUMBER
        CHECK (CARD_NUMBER REGEXP '^[A-Z]{2}[0-9]{22}$')
);

CREATE TABLE ADMIN_ (
USERNAME VARCHAR(40) PRIMARY KEY,
CURRENT_ACCOUNT VARCHAR(40),
FOREIGN KEY (USERNAME) REFERENCES PROFILE_ (USERNAME)
);

CREATE TABLE SHOE (
ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
PRICE DOUBLE NOT NULL,
MODEL VARCHAR(30) NOT NULL,
SIZE DOUBLE NOT NULL,
EXCLUSIVE ENUM ('TRUE', 'FALSE') DEFAULT 'FALSE',
MANUFACTER_DATE DATE,
COLOR VARCHAR(20) NOT NULL,
ORIGIN VARCHAR(20) NOT NULL,
BRAND VARCHAR(20) NOT NULL,
RESERVED ENUM('TRUE', 'FALSE') DEFAULT 'FALSE',
STOCK INT
);

CREATE TABLE REVIEW(
ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
ID_SHOE INT,
USERNAME VARCHAR(40),
DESCRIPTION VARCHAR(40),
RATING INT NOT NULL,
DATE_ DATE,
FOREIGN KEY (ID_SHOE) REFERENCES SHOE (ID),
FOREIGN KEY (USERNAME) REFERENCES USER_ (USERNAME));

CREATE TABLE ORDER_ (
ORDER_ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
USERNAME VARCHAR(40),
SHOE_ID INT,
DATE_ DATE NOT NULL,
QUANTITY INT NOT NULL,
FOREIGN KEY (USERNAME) REFERENCES USER_ (USERNAME),
FOREIGN KEY (SHOE_ID) REFERENCES SHOE (ID));

